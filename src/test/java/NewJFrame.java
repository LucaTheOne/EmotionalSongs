        bar.setValue(27, "Voti", "Materia6");
        
        JFreeChart barChart = ChartFactory.createBarChart("Voti anno x", "Materie anno", "Voto", bar, PlotOrientation.VERTICAL, false, true, false);
        barChart.setBackgroundPaint(Color.YELLOW);
        barChart.getTitle().setPaint(Color.GREEN);
        CategoryPlot plot = barChart.getCategoryPlot();
        
        ChartPanel panelChart = new ChartPanel( barChart);
